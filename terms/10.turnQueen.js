// Ход ферзя
// Шахматный ферзь ходит по диагонали, горизонтали или вертикали. Даны две различные
// клетки шахматной доски, определите, может ли ферзь попасть с первой клетки на вторую одним ходом.

function Queen(a, b, c, d) {
    let x = a / b
    let y = c / d
    let x1 = a + b
    let y1 = c + d
    if ((a === c) || (b === d) || (x === y) || (x1 === y1)) {
        return "yes"
    } else {
        return "no"
    }
}

console.log(Queen(6, 5, 2, 5))